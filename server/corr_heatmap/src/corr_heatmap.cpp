#endif
{
	var idx;
	var tmp,t;
	var ret = new Array();
	var pos=0,ct=0;
	idx = str.indexOf("\n");
	if (idx == -1)return ret;
	tmp = str.substring(0, idx);
	if (tmp.indexOf(",")==-1)return ret;
	while (true) {
		if (tmp == "")break;
		idx = tmp.indexOf(",",pos);
		if (idx != -1) {
			t = tmp.substring(pos, idx);
		}
		else t = tmp.substring(pos);
		if (t.indexOf("#") == -1) {
			ret[ct] = t;
			ct++;
		}
		if (idx == -1)break;
		pos = idx + 1;
	}
	return ret;
}

